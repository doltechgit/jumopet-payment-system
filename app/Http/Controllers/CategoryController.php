    public function get_category($id)
    {

        $category = Category::where('slug',  $id)->first();
        // $clients = Client::where('name', $id)->orWhere('phone', $id)->get();
        $products = $category->products;
        if (count($products) > 0) {
            return response()->json([
                'status' => 200,
                'products' => $products,
            ]);
        } else {
            http_response_code(404);
            return response()->json([
                'error' => 'Not Found'
            ], 404);
        }
    }

